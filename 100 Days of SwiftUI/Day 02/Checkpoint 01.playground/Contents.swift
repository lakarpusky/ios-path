import UIKit

// 1. Creates a constant holding any temperature in Celsus
// 2. Converts it to Fahrenheit by multiplying by 9, dividing by 5, then adding 32
// 3. Prints the result for the user, showing both the Celsius and Fahrenheit vaues

let celsius = 45.0
let fahrenheit = (celsius * 9 / 5) + 32

print("Value in celsius is \(celsius)°C")
print("Value in fahrenheit is \(fahrenheit)°F")
